    private void adicionar_Componente(String nome_componente,float valor){
    try{    
        int posicao=listcomponentes.getNextMatch(nome_componente, 0, Position.Bias.Forward);
        listcomponentes.setSelectedIndex(posicao);
        lista1.addElement(nome_componente);
        total+=valor;
        txttotal.setText(ft.format(total));
        listcomponentes.setSelectedIndex(listcomponentes.getSelectedIndex());
    return;
    }
    catch(Exception e){}    
        lista1.addElement(nome_componente);
        listcomponentes.setModel(lista1);
        total+=valor;
        txttotal.setText(ft.format(total));       
    
    }
    
    private void remover_Componente(String nome_componente,float valor){
     try{    
        int posicao=listcomponentes.getNextMatch(nome_componente, 0, Position.Bias.Forward);
        listcomponentes.setSelectedIndex(posicao);
        lista1.removeElement(nome_componente);
        total-=valor;
        txttotal.setText(ft.format(total));
        listcomponentes.setSelectedIndex(listcomponentes.getSelectedIndex());
    return;
    }
    catch(Exception e){}    
        lista1.removeElement(nome_componente);
        listcomponentes.setModel(lista1);
        total-=valor;
        txttotal.setText(ft.format(total));       
       
        
    }
