    private void btnguardaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnguardaActionPerformed
        ligacaoMySql bd=new ligacaoMySql();
        bd.criarLigacaoMySql();

        try{
            Connection cn=bd.criarLigacaoMySql();
            Statement st =cn.createStatement();
            LocalDateTime sistema=LocalDateTime.now();
            DateTimeFormatter dtf=DateTimeFormatter.ofPattern("dd-MM-yyyy");
            int max = lista1.getSize();
            for (int posicao = 0;posicao < max;posicao++){
                st.executeUpdate("insert into tab_componentes (Componentes,Data) "
                        + "values('"+lista1.getElementAt(posicao)+ "','"+dtf.format(sistema)+"')");
                JOptionPane.showMessageDialog(this,
                    "Dados guardados",
                    "Componentes Info",
                    JOptionPane.INFORMATION_MESSAGE);
            }

            lista1.clear();

        }
        catch(SQLException e){
            JOptionPane.showMessageDialog(this,
                "Erro ao guardar os dados! "+e.getMessage(),
                "Componentes Info",
                JOptionPane.ERROR_MESSAGE);
        }

    }//GEN-LAST:event_btnguardaActionPerformed

