    class Imprimir implements Printable{
        public int print(Graphics g, PageFormat f, int pageIndex){
            if (pageIndex==0){
                /* trabalhar a impressão*/
                Font f1=new Font("Tahoma",Font.BOLD,12);
                Font f2=new Font("Tahoma",Font.PLAIN,10);
                Font f3=new Font("Tahoma",Font.PLAIN,8);
                
                g.setFont(f1);
                g.setColor(Color.blue);
                g.drawString("---------------------------------", 25,40);
                g.drawString("Componentes Info", 25,50 );
                g.drawString("---------------------------------", 25,60);
                g.setFont(f3);
                g.setColor(Color.gray);
                g.drawLine(25, 80, 200, 80);
                g.drawString("Produto", 25, 90);
                g.drawLine(25, 95, 200, 95);
                
               
                int y=105;
                for(int i=0;i<lista1.getSize();i++){
                    g.drawString(lista1.getElementAt(i).toString(), 25, y);
                    
                    
                    y+=10;
                }
                
                g.drawLine(25, y, 200, y);
                g.drawString("Total:", 25, y+10);
                g.drawString(txttotal.getText(), 47, y+10); 
                g.drawString("Data:", 25, y+20);
                g.drawString(lbldata.getText(), 45, y+20);
                g.drawLine(25, y+25, 200, y+25);
                g.setColor(Color.blue);
                g.setFont(f2);
                g.drawString("Obrigado por nos escolher.Volte sempre!", 25, y+50);
                return PAGE_EXISTS;
            }
            else{
                return NO_SUCH_PAGE;
            }
               
        }
        
    }    
