    private void adicionar_Produto(String nome_produto,float preco){
    try{    
        int posicao=listdescricao.getNextMatch(nome_produto, 0, Position.Bias.Forward);
        listquant.setSelectedIndex(posicao);
        int qtd=listquant.getSelectedValue()+1;
        lista2.setElementAt(qtd,posicao);
        
        listtotal.setSelectedIndex(posicao);
        lista4.setElementAt(ft1.format(qtd*preco),posicao);
        
        total+=preco;
        txttotal.setText(ft.format(total));
        
        listdescricao.setSelectedIndex(listquant.getSelectedIndex());
        listpreco.setSelectedIndex(listquant.getSelectedIndex());
        listtotal.setSelectedIndex(listquant.getSelectedIndex());
        
        return;
    }
    catch(Exception e){}    
    // adicionar à lista da descrição
        lista1.addElement(nome_produto);
        listdescricao.setModel(lista1);

        // adicionar à lista da quantidade
        lista2.addElement(1);
        listquant.setModel(lista2);

        // adicionar à lista do preço
        lista3.addElement(preco);
        listpreco.setModel(lista3);
        // adicionar à lista do total
        lista4.addElement(preco);
        listtotal.setModel(lista4);   

        total+=preco;
        txttotal.setText(ft.format(total));       
    
    }
