    private void btnquantActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnquantActionPerformed
        if(listdescricao.getSelectedIndex()<0){
            JOptionPane.showMessageDialog(this,
                "Não existem produtos selecionados",
                "Pastelaria Papa Tudo",
                JOptionPane.ERROR_MESSAGE);
            return;
        }
        int posicao=listdescricao.getSelectedIndex();
        int qtd = listquant.getSelectedValue();
        float prc = listpreco.getSelectedValue();
        total -= (qtd*prc);
        qtd = Integer.parseInt(JOptionPane.showInputDialog(this, "Indique a quantidade desejada:",
            "Pastelaria Papa Tudo",JOptionPane.QUESTION_MESSAGE));

    if(qtd<=0){
        qtd = Integer.parseInt(JOptionPane.showInputDialog(this, "Indique novamente a quantidade desejada:",
            "Pastelaria Papa Tudo",JOptionPane.QUESTION_MESSAGE));
    }
    lista2.setElementAt(qtd,posicao);
    listpreco.setSelectedIndex(posicao);
    listtotal.setSelectedIndex(posicao);
    lista4.setElementAt(ft1.format(qtd*prc),posicao);
    total+= (qtd*prc);
    txttotal.setText(ft.format(total));
    }//GEN-LAST:event_btnquantActionPerformed

